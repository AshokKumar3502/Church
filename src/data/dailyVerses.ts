// Helper to get verse by day (1-365)
// If day > 31, it cycles through the available ones for now
export const getVerseForDay = (day: number): Verse => {
  const totalAvailable = Object.keys(DAILY_VERSES).length;
  const index = ((day - 1) % totalAvailable) + 1;
  return DAILY_VERSES[index];
};
